def sum(a, b):
    """
    Return the sum of two numbers.

    Args:
        a (int/float): first number
        b (int/float): second number

    Return:
        int/float: sum of a and b
    """
    return a + b


def subtraction(a, b):
    """
    Return the difference between two numbers.

    Args:
        a (int/float): first number
        b (int/float): second number

    Return:
        int/float: difference of a minus b
    """
    return a - b
